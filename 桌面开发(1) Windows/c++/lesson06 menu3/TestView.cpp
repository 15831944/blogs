// CTestView 消息处理程序


void CTestView::OnChar(UINT nChar, UINT nRepCnt, UINT nFlags)
{
	// TODO: 在此添加消息处理程序代码和/或调用默认值
	CClientDC dc(this);
	if (0x0d == nChar)
	{
		if (0 == ++m_nIndex)
		{
			// 创建弹出菜单
			m_menu.CreatePopupMenu();
			GetParent()->GetMenu()->AppendMenuA(MF_POPUP, (UINT)m_menu.m_hMenu, "PhoneBook");		// 菜单栏属于框架类
			GetParent()->DrawMenuBar();		// 框架类重绘菜单栏
		}
		m_menu.AppendMenuA(MF_STRING, 111, m_strLine.Left(m_strLine.Find(' ')));
		m_strArray.Add(m_strLine);
		m_strLine.Empty();
		Invalidate();
	}
	else
	{
		m_strLine += (char)nChar;
		dc.TextOutA(0, 0, m_strLine);
	}
	CView::OnChar(nChar, nRepCnt, nFlags);
}
